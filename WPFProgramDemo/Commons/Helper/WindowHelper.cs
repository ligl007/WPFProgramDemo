        /// <summary>
        /// 通过将窗口样式设置为WS_EX_TOOLWINDOW来避免窗口在Alt+Tab窗口列表中出现。
        /// </summary>
        /// <param name="window">要隐藏的窗口。</param>
        public static void HideWindowInSwitcher(Window window)
        {
            IntPtr handle = new WindowInteropHelper(window).Handle;
            int oldStyle = GetWindowLong(handle, WindowlongItem.GWL_EXSTYLE);
            IntPtr newStyle = new IntPtr(oldStyle | (int)WindowExStyle.WS_EX_TOOLWINDOW);
            SetWindowLong(handle, WindowlongItem.GWL_EXSTYLE, newStyle);
        }

        #region Nested type: MINMAXINFO

        [StructLayout(LayoutKind.Sequential)]
        internal struct MINMAXINFO
        {
            public POINT ptReserved;
