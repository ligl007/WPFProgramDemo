        /// <summary>
        /// 修复WindowStyle为None的WPF窗口在最大化时覆盖任务栏的问题。
        /// </summary>
        /// <param name="wpfWindow">要修复的WPF窗口。</param>
        public static void CorrectMaximization(Window wpfWindow)
        {
            if (wpfWindow == null)
            {
                return;
            }

            if (wpfWindow.WindowState == WindowState.Maximized)
            {
                wpfWindow.WindowState = WindowState.Normal;
                wpfWindow.Loaded += delegate { wpfWindow.WindowState = WindowState.Maximized; };
            }

            wpfWindow.SourceInitialized += delegate
            {
                IntPtr handle = (new WindowInteropHelper(wpfWindow)).Handle;
                HwndSource source = HwndSource.FromHwnd(handle);
                if (source != null)
                {
                    source.AddHook(WindowProc);
                }
            };
        }

        private static IntPtr WindowProc(IntPtr hwnd, int msg, IntPtr wParam, IntPtr lParam, ref bool handled)
        {
            switch (msg)
            {
                case 0x0024:
                    WmGetMinMaxInfo(hwnd, lParam);
                    handled = true;
                    break;
            }

            return (IntPtr)0;
        }

        private static void WmGetMinMaxInfo(IntPtr hwnd, IntPtr lParam)
        {
            var mmi = (MINMAXINFO)Marshal.PtrToStructure(lParam, typeof(MINMAXINFO));

            // Adjust the maximized size and position to fit the work area of the correct monitor
            int MONITOR_DEFAULTTONEAREST = 0x00000002;
            IntPtr monitor = MonitorFromWindow(hwnd, MONITOR_DEFAULTTONEAREST);

            if (monitor != IntPtr.Zero)
            {
                var monitorInfo = new MONITORINFO();
                GetMonitorInfo(monitor, monitorInfo);
                RECT rcWorkArea = monitorInfo.rcWork;
                RECT rcMonitorArea = monitorInfo.rcMonitor;
                mmi.ptMaxPosition.x = Math.Abs(rcWorkArea.left - rcMonitorArea.left);
                mmi.ptMaxPosition.y = Math.Abs(rcWorkArea.top - rcMonitorArea.top);
                mmi.ptMaxSize.x = Math.Abs(rcWorkArea.right - rcWorkArea.left);
                mmi.ptMaxSize.y = Math.Abs(rcWorkArea.bottom - rcWorkArea.top);
            }

            Marshal.StructureToPtr(mmi, lParam, true);
        }

        /// <summary>
        /// 执行抖动窗体
        /// </summary>
        /// <param name="talkWindow"></param>
        public static void ShakeWindow(Window talkWindow)
        {
            if (talkWindow.WindowState != WindowState.Normal)
            {
                talkWindow.WindowState = WindowState.Normal;
            }
            int shake = 0;
            double OriginalLeft = talkWindow.Left;
            double OriginalTop = talkWindow.Top;
            double newLeftOne = OriginalLeft - 8;
            double newTopTwo = OriginalTop - 8;
            while (shake < 3)
            {
                talkWindow.Left = newLeftOne;
                System.Threading.Thread.Sleep(50);
                talkWindow.Top = newTopTwo;
                System.Threading.Thread.Sleep(50);
                talkWindow.Left = OriginalLeft;
                System.Threading.Thread.Sleep(50);
                talkWindow.Top = OriginalTop;
                System.Threading.Thread.Sleep(50);
                shake++;
            }
            talkWindow.Left = OriginalLeft;
            talkWindow.Top = OriginalTop;

        }

